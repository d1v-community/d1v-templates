function textColumn(name, dbName, options = {}) {
  return {
    kind: "text",
    name,
    dbName,
    notNull: options.notNull ?? false,
    primaryKey: options.primaryKey ?? false,
    references: options.references,
  };
}

function timestampColumn(name, dbName) {
  return {
    kind: "timestamp",
    name,
    dbName,
    notNull: true,
    defaultNow: true,
  };
}

function withTimestamps(columns) {
  return [
    ...columns,
    timestampColumn("createdAt", "created_at"),
    timestampColumn("updatedAt", "updated_at"),
  ];
}

